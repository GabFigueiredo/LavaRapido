    private void configurarTabela() {
        modelo = new DefaultTableModel(
            new String[] {"ID", "Nome", "Preço", "Descrição"}, 0
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return (column != 0 && column != 1); // ID não é editável
            }
        };

        tabelaServicos.setModel(modelo);
    }
    
    private void adicionarListenerDeEdicao() {
        modelo.addTableModelListener(e -> {
            if (e.getType() == TableModelEvent.UPDATE) {
                int linha = e.getFirstRow();

                // Recupera os valores editados
                int id = (int) modelo.getValueAt(linha, 0);
                String precoStr = modelo.getValueAt(linha, 2).toString();
                String descricao = (String) modelo.getValueAt(linha, 3);
                
                Double preco;
                try {
                    preco = Double.parseDouble(precoStr);
                } catch (NumberFormatException ex) {
                    JOptionPane.showMessageDialog(null, "O preço deve ser um número válido.", "Erro", JOptionPane.ERROR_MESSAGE);
                    return;
                }

                try {
                    servicoController.editarTipoServico(id, preco, descricao);
                    System.out.println("Serviço atualizado com sucesso.");
                    JOptionPane.showMessageDialog(null, "Serviço atualizado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, "Erro ao atualizar serviço: " + ex.getMessage());
                }
            }
        });
    }
    
    private void atualizarTabela() throws SQLException {
        this.listaServicos = servicoController.listarTiposServico();

        modelo.setRowCount(0); // Limpa a tabela

        for (TipoServico servico : listaServicos) {
            modelo.addRow(new Object[] {
                servico.getId(),
                servico.getNome(),
                servico.getPreco(),
                servico.getDescricao(),
            });
        }
    }

