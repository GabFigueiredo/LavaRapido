    private void criarClienteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_criarClienteBtnActionPerformed
        String Nome = NomeInput.getText().trim();
        String Preco = PrecoInput.getText().trim();
        String Descricao = DescricaoInput.getText().trim();
                

        // Verificações básicas
        if (Nome.isEmpty()) {
            JOptionPane.showMessageDialog(null, "O nome é obrigatório.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (Preco.isEmpty()) {
            JOptionPane.showMessageDialog(null, "O preço é obrigatório.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        double precoConvertido;
        try {
            precoConvertido = Double.parseDouble(Preco);
            // Se quiser usar o valor convertido depois, use a variável precoConvertido
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "O preço deve ser um número válido (use ponto, não vírgula).", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (Descricao.isEmpty()) {
            JOptionPane.showMessageDialog(null, "A descrição é obrigatória.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            TipoServico novoServico = new TipoServico(0, Nome, Descricao, precoConvertido);
            servicoController.cadastrarTipoServico(novoServico);
            JOptionPane.showMessageDialog(null, "Serviço criado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            atualizarTabela();
        } catch (SQLException ex) {
            Logger.getLogger(TelaClientes.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_criarClienteBtnActionPerformed


